def ensure_unique_names(name_dict):
    # Transform the names
    
    name_counts = Counter(map.values())
    
    # Create a dictionary to track the version numbers for repeated names
    version_tracker = Counter()
    
    unique_names = {}
    
    for key, name in map.items():
        if name_counts[name] > 1:
            version_tracker[name] += 1
            unique_name = f"{name} V{version_tracker[name]}"
        else:
            unique_name = name
        unique_names[key] = unique_name
    
    return unique_names

